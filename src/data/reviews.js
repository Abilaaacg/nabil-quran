// مولد شبه عشوائي ثابت (deterministic) حسب index
function seededValue(index, mod) {
  const x = ((index * 2654435761) >>> 0) % mod
  return x
}

function generateReviews(count) {
  const reviews = []
  for (let i = 0; i < count; i++) {
    const fi = seededValue(i, FIRST_NAMES.length)
    const li = seededValue(i + 7919, LAST_NAMES.length)
    const ti = seededValue(i + 1327, TEMPLATES.length)
    const feat = seededValue(i + 4219, FEATURES.length)
    const stars = i % 20 === 0 ? 4 : 5 // 95% يعطون 5 نجوم

    const name = `${FIRST_NAMES[fi]} ${LAST_NAMES[li]}`
    const text = TEMPLATES[ti].replace('{feature}', FEATURES[feat])

    reviews.push({ name, stars, text })
  }
  return reviews
}

